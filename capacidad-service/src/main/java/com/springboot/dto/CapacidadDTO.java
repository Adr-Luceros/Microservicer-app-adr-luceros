package com.springboot.dto;

public class CapacidadDTO {

	private float volumen;
	
	public CapacidadDTO() {
		
	}

	public CapacidadDTO(float volumen) {
		
		this.volumen = volumen;
	}

	public float getVolumen() {
		return volumen;
	}

	public void setVolumen(float volumen) {
		this.volumen = volumen;
	}
	
	
}
